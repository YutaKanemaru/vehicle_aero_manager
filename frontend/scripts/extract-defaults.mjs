/**
 * Extract Pydantic TemplateSettings defaults from backend/template_defaults.json
 * and emit frontend/src/api/templateDefaults.ts.
 *
 * Called automatically by `npm run generate-api`.
 * Do not run this script directly unless backend/template_defaults.json is up to date.
 */

import { readFileSync, writeFileSync, mkdirSync } from "fs";
import { fileURLToPath } from "url";
import { dirname, resolve } from "path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const inputPath = resolve(__dirname, "../../backend/template_defaults.json");
const outputPath = resolve(__dirname, "../src/api/templateDefaults.ts");

/** Convert a JSON value to a TypeScript literal string, with indentation. */
function toTs(value, depth = 0) {
  const pad = "  ".repeat(depth);
  const innerPad = "  ".repeat(depth + 1);

  if (value === null) return "null";
  if (typeof value === "boolean") return String(value);
  if (typeof value === "number") return String(value);
  if (typeof value === "string") return JSON.stringify(value);

  if (Array.isArray(value)) {
    if (value.length === 0) return "[]";
    const items = value.map((v) => `${innerPad}${toTs(v, depth + 1)}`);
    return `[\n${items.join(",\n")},\n${pad}]`;
  }

  // object
  const entries = Object.entries(value);
  if (entries.length === 0) return "{}";
  const lines = entries.map(([k, v]) => `${innerPad}${k}: ${toTs(v, depth + 1)}`);
  return `{\n${lines.join(",\n")},\n${pad}}`;
}

const raw = JSON.parse(readFileSync(inputPath, "utf-8"));

const tsContent = `\
// Auto-generated by scripts/extract-defaults.mjs — do not edit manually.
// Regenerate with: npm run generate-api
// Source: backend/app/schemas/template_settings.py → TemplateSettings().model_dump()

export const templateDefaults = ${toTs(raw, 0)} as const;

export type TemplateDefaultsType = typeof templateDefaults;
`;

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, tsContent, "utf-8");
console.log(`✓ Generated ${outputPath}`);
